/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} root
 * @return {number[][]}
 */
var levelOrderBottom = function (root) {
    if (root == null) {
        return []
    }
    let levelArr = [root]
    const recordArr = []
    while (levelArr.length > 0) {
        const arr = []
        const temp = []
        for (let node of levelArr) {
            arr.push(node.val)
            node.left && temp.push(node.left)
            node.right && temp.push(node.right)
        }
        levelArr = temp
        recordArr.push(arr)
    }
    return recordArr.reverse()
};
