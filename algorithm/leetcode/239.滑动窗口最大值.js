// @lc code=start
/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number[]}
 */
var maxSlidingWindow = function(nums, k) {
    if (nums.length === 1 || k === 1) {
        return nums
    }
    let maxArr = []
    let que = [] // 存储下标且队头始终是最大元素的下标
    for (let i = 0; i < nums.length; i++) {
        if (i - que[0] >= k) { // 滑动窗口外的元素直接从队头删除
            que.shift()
        }
        while(nums[que[que.length - 1]] <= nums[i]) {
            que.pop() // 新加入的数比原来的数都要大，则直接弹出
        }
        que.push(i)

        if (i >= k-1) { // 第一个滑动窗口生成最大值是从k之前的元素生成的
            maxArr.push(nums[que[0]])
        }
    }
    return maxArr
};
