var inorderTraversal = function (root) { // 迭代
    const list = []
    const stack = [] // 使用栈来实现
    let cur = root
    while (cur != null || stack.length > 0) {
        if (cur != null) {
            stack.push(cur)
            cur = cur.left
        } else {
            cur = stack.pop()
            list.push(cur.val)
            cur = cur.right
        }
    }
    return list
};
